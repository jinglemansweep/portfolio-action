/**
 * Render a brand icon by type string. Falls back to Simple Icons CDN.
 */
export function brandIcon(type, cls = 'h-4 w-4') {
  const icons = {
    github: iconGithub,
    linkedin: iconLinkedin,
    x: iconX,
    tiktok: iconTiktok,
    mastodon: iconMastodon,
  };
  if (icons[type]) return icons[type](cls);
  return html`<img
    src="https://cdn.simpleicons.org/${type}/white"
    alt="${type}"
    class=${cls}
    loading="lazy"
  />`;
}
